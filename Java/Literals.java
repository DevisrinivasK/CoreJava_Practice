public class Literals {
    public static void main(String[] a){
        //Literals
        //Binary 
        int num= 0b1011;
        char c='a';
        c++;
        //Hexadecimal
        int n2=0x7AC0;
        //underscore to differentiate zeros
        int n3=1_00_00_000;
        System.out.println(num);
        System.out.println(c);
        System.out.println(n2);
        System.out.println(n3);
        
    }
}
    

